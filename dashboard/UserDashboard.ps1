    # Build the top nav bar HTML. Marks the current page so CSS can highlight it.
    # External items get target=_blank+rel=noopener (harmless for same-tab apps,
    # and means Browse/Request open in a new tab, leaving the dashboard intact).
    function Get-NavHtml([string]$current) {
        $items = Get-PodeState -Name 'NavItems'
        $parts = $items | ForEach-Object {
            if ($_.External) {
                "<a href=`"$($_.Href)`" class=`"nav-link`" target=`"_blank`" rel=`"noopener`">$($_.Title)</a>"
            } else {
                "<a href=`"$($_.Href)`" class=`"nav-link`">$($_.Title)</a>"
            }
        }
        return "<nav class=`"topnav`">$($parts -join '')</nav>"
    }

    # Read template (mtime-cached) with a minimal fallback. Returns the raw
    # template HTML with tokens still present.
    function Get-TemplateHtml {
        $cfgDir  = Get-PodeState -Name 'CfgDir'
        $tplPath = Join-Path $cfgDir 'template.html'
        $html    = Read-CachedFile $tplPath
        if (-not $html) {
            "TEMPLATE ERROR: $tplPath not found — serving minimal fallback page" | Out-Default
            $html = @"
<!DOCTYPE html><html><head><meta charset="utf-8"><title>...Gunit!</title></head>
<body style="font-family:sans-serif;background:#1c1f24;color:#ccc;padding:40px">
{{NAV}}<h1 class="brand-title">...Gunit!</h1><p>{{GREETING}}</p><p>Signed in as {{EMAIL}}</p>
{{LINKS}}<h2>My Settings</h2>{{TOGGLES}}<p id="status"></p>
<div>{{CONTENT}}</div>
<p style="opacity:.5">(template file not found at $tplPath)</p>
<script>
let statusTimeout;
async function saveToggle(el){const s=document.getElementById('status');clearTimeout(statusTimeout);s.innerText='Saving...';
try{const r=await fetch('/toggle',{method:'POST',headers:{'Content-Type':'application/json'},
body:JSON.stringify({key:el.dataset.key,value:el.checked})});s.innerText=r.ok?'Saved.':'Error saving.';
if(!r.ok)el.checked=!el.checked;}catch(e){s.innerText='Network error.';el.checked=!el.checked;}
statusTimeout=setTimeout(()=>{s.innerText='';},2500);}
</script></body></html>
"@
        }
        return $html
    }

    # Substitute all tokens. Any token not supplied for a given page is replaced
    # with '' so secondary pages (lists, how-to-use) don't show stray {{...}}.
    function Resolve-Tokens([string]$html, [hashtable]$tok) {
        foreach ($name in @('NAV','HOME','CONTENT','GREETING','EMAIL','LINKS','TOGGLES','INSTRUCTIONS')) {
            $val = if ($tok.ContainsKey($name)) { [string]$tok[$name] } else { '' }
            $html = $html.Replace("{{$name}}", $val)
        }
        return $html
    }

    # ------------------------------------------------------------------------
    #  IMAGE ROUTES for editable image dirs under the config dir.
    #  Served via explicit Add-PodeRoute (NOT Add-PodeStaticRoute): on Pode
    #  2.13.2 the static routes registered but silently 404'd on every file.
    #  This uses the same dynamic-route + Get-PodeState pattern as /lists/:name
    #  (which works), with a strict single-segment filename gate to block path
    #  traversal, then streams the file with Write-PodeFileResponse.
    #    /screenshots/<file>  <- <cfgDir>/screenshots       (How-To markdown images)
    #    /covers/<file>       <- <cfgDir>/lists/covers       (book-list cover images)
    #
    #  NOTE (v18): covers live in <cfgDir>/lists/covers (next to the list .md
    #  files that reference them, where link-md writes them), NOT <cfgDir>/covers.
    # ------------------------------------------------------------------------
    "STATIC: /screenshots/:file -> $(Join-Path $cfgDir 'screenshots')" | Out-Default
    Add-PodeRoute -Method Get -Path '/screenshots/:file' -ScriptBlock {
        $file = $WebEvent.Parameters['file']
        if ([string]::IsNullOrWhiteSpace($file) -or $file -notmatch '^[A-Za-z0-9._-]+$') {
            Write-PodeTextResponse -Value 'bad filename' -StatusCode 400; return
        }
        $path = Join-Path (Join-Path (Get-PodeState -Name 'CfgDir') 'screenshots') $file
        if (Test-Path $path -PathType Leaf) {
            Write-PodeFileResponse -Path $path
        } else {
            Write-PodeTextResponse -Value 'not found' -StatusCode 404
        }
    }

    "STATIC: /covers/:file -> $(Join-Path $cfgDir 'lists/covers')" | Out-Default
    Add-PodeRoute -Method Get -Path '/covers/:file' -ScriptBlock {
        $file = $WebEvent.Parameters['file']
        if ([string]::IsNullOrWhiteSpace($file) -or $file -notmatch '^[A-Za-z0-9._-]+$') {
            Write-PodeTextResponse -Value 'bad filename' -StatusCode 400; return
        }
        $path = Join-Path (Join-Path (Get-PodeState -Name 'CfgDir') 'lists/covers') $file
        if (Test-Path $path -PathType Leaf) {
            Write-PodeFileResponse -Path $path
        } else {
            Write-PodeTextResponse -Value 'not found' -StatusCode 404
        }
    }

    # Favicon: served the same proven dynamic-route way (static routes 404 on
    # this Pode build). The <head> link in template.html points at /favicon.ico.
    # Drop favicon.ico into <cfgDir>; if it's absent the route still answers but
    # returns 404 (harmless — the browser just shows its default icon). To swap
    # the icon, replace the file — no rebuild needed; only this wiring is baked.
    "STATIC: /favicon.ico -> $(Join-Path $cfgDir 'favicon.ico')" | Out-Default
