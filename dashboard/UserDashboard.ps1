    # ------------------------------------------------------------------------
    Add-PodeRoute -Method Get -Path '/' -ScriptBlock {
      try {
        $email = Get-VerifiedUser
        if (-not $email) {
            Write-PodeTextResponse -Value 'Not authenticated.' -StatusCode 401
            return
        }
        $prefs    = Get-UserPrefs $email
        $greeting = Resolve-Greeting $email
        $links    = Get-PodeState -Name 'DashLinks'
        $defs     = Get-PodeState -Name 'ToggleDefs'

        $linksHtml = ($links | ForEach-Object {
            $label = $_.Title
            if ($_.Sub) { $label += "<span class=`"custom-link-sub`">$($_.Sub)</span>" }
            if ($_.External) {
                "<a href=`"$($_.Url)`" class=`"custom-link`" target=`"_blank`" rel=`"noopener`">$label</a>"
            } else {
                "<a href=`"$($_.Url)`" class=`"custom-link`">$label</a>"
            }
        }) -join "`n"

        $togglesHtml = ($defs | Where-Object {
            -not $_.Users -or ($_.Users -contains $email)
        } | ForEach-Object {
            $checked = if ($prefs[$_.Key]) { 'checked' } else { '' }
            @"
<label class="toggle">
  <input type="checkbox" data-key="$($_.Key)" $checked onchange="saveToggle(this)" />
  <span>$($_.Label)</span>
</label>
"@
        }) -join "`n"

        $html = Get-TemplateHtml
        $homeHtml = @"
<div class="custom-section">
  <h1 class="brand-title">...Gunit!</h1>
  <p class="greeting">$greeting</p>
  <p class="whoami">Signed in as <strong>$email</strong></p>
  $linksHtml
</div>
<div class="custom-section" style="border-top: 2px solid rgba(255,255,255,0.05); padding-top: 30px;">
  $togglesHtml
  <p id="status"></p>
</div>
"@
        $html = Resolve-Tokens $html @{
            NAV  = (Get-NavHtml '/')
            HOME = $homeHtml
        }
        Write-PodeHtmlResponse -Value $html
      } catch {
        Write-PodeTextResponse -StatusCode 500 -Value "ERROR in GET /: $($_.Exception.Message)`n`n$($_.ScriptStackTrace)"
      }
    }

