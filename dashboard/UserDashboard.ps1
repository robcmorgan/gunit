    # --- lists index: render web/lists.md (hand-written index of links). The
    #     index links to subpages (/lists/<name>) and/or external URLs.
    function Get-ListsHtml { return (Get-MarkdownHtml 'lists.md') }

    # --- a single named sublist: render web/lists/<name>.md as its own page.
    #     <name> is sanitised to one path segment (letters, digits, dash,
    #     underscore) so a request can't escape the lists dir (no .., no /).
    #     Returns '' if the name is invalid or the file is absent.
    function Get-SubListHtml([string]$name) {
        if ([string]::IsNullOrWhiteSpace($name)) { return '' }
        if ($name -notmatch '^[A-Za-z0-9_-]+$') {
            "WARN: rejected sublist name '$name' (illegal chars)" | Out-Default
            return ''
        }
        $html = Get-MarkdownHtml (Join-Path 'lists' "$name.md")
        if (-not $html) { return '' }
        # Book numbers are AUTO-GENERATED by a CSS counter (see template), not
        # typed in the markdown. Here we:
        #  1) strip any leading "NNN." a list might still contain (so old numbered
        #     lists don't double up), then
        #  2) inject an EMPTY <span class="booknum"></span> into every book <h4>
        #     as the hook the CSS counter writes the number into.
        # First strip a leading number if present:
        $html = [regex]::Replace($html, '(?<=<h4[^>]*>)\s*\d+\.\s*', '')
        # Then add the counter hook to every h4 (idempotent: only if absent):
        # inject the counter hook with NO trailing space — the span is an
        # inline-block of fixed width that spans rail-1→rail-2, so a trailing
        # space would push the title ~4px past rail 2 (misaligning it with the
        # body, which is at rail 2).
        $html = [regex]::Replace(
            $html,
            '(<h4[^>]*>)(?!\s*<span class="booknum")',
            '$1<span class="booknum"></span>'
        )
        # NOTE: a future "[resetnum]" / "[resetnum N]" marker is intended to
        # restart numbering mid-list, but a correct CSS-counter reset needs a
        # per-section wrapper (a bare counter-reset mid-list nests rather than
        # resets). Not implemented yet — for now we just STRIP any [resetnum]
        # marker so it never shows as literal text in a list.
        $html = [regex]::Replace($html, '(?s)<p>\s*\[resetnum(?:\s+\d+)?\]\s*</p>', '')
        # Inside author <h5> lines, turn " | " into a styled divider span (more
        # space + lighter colour). Scoped to h5 so pipes elsewhere are untouched.
        $html = [regex]::Replace(
            $html,
            '(<h5[^>]*>)(.*?)(</h5>)',
            {
                param($m)
                $inner = $m.Groups[2].Value -replace '\s*\|\s*', '<span class="sep">|</span>'
                $m.Groups[1].Value + $inner + $m.Groups[3].Value
            }
        )
        # Book covers: list markdown references a cover as plain ![](covers/<id>.jpg),
        # optionally wrapped in a link: [![](covers/<id>.jpg)](url). ConvertFrom-Markdown
        # renders an <img src="covers/<id>.jpg" .../>. Two order-independent passes:
        #   1) rewrite ANY cover src to an ABSOLUTE "/covers/<id>.jpg". The page is
        #      served at /lists/<name>, so a RELATIVE "covers/..." resolves to
        #      /lists/covers/... and 404s. Matches both "covers/..." and a stray
        #      "/lists/covers/..." just in case. Done first so pass 2 keys off /covers/.
        #   2) add class="bookcover" to any <img> whose src is /covers/... and that
        #      isn't already classed. Independent of attribute order, and works whether
        #      or not the <img> is wrapped in an <a>. Idempotent on re-render.
        $html = [regex]::Replace($html, '\bsrc="(?:/lists/)?covers/', 'src="/covers/')
        $html = [regex]::Replace(
            $html,
            '<img(?![^>]*\bclass=)([^>]*\bsrc="/covers/[^"]+"[^>]*?)/?>',
            '<img class="bookcover"$1/>'
        )

        # Per-book structure. The title (<h4>) stays on its own at the top. The
        # rest of the book is split into:
        #   .book-head : the cover + the h5 rows (author/tags/footer), laid out as
        #                a flex row with the h5 group vertically CENTRED against
        #                the cover's height.
        #   then the <p> description paragraphs, which follow .book-head and so
        #                always start BELOW the cover, full width at rail 1.
        # Coverless books: .book-head still holds the h5 rows (no cover), and the
        # body follows — looks the same as a normal stacked entry.
        $coverPat = '(?s)(?:<p>\s*)?(?:<a\b[^>]*>\s*)?<img\b[^>]*\bclass="bookcover"[^>]*/?>(?:\s*</a>)?(?:\s*</p>)?'
        $h4idx = $html.IndexOf('<h4')
        if ($h4idx -ge 0) {
            $intro = $html.Substring(0, $h4idx)
            $rest  = $html.Substring($h4idx)
            $chunks = [regex]::Split($rest, '(?=<h4)') | Where-Object { $_ -ne '' }
            $books = foreach ($chunk in $chunks) {
                $mh = [regex]::Match($chunk, '(?s)^\s*<h4\b.*?</h4>')
                if (-not $mh.Success) { $chunk; continue }
                $title = $mh.Value
                $after = $chunk.Substring($mh.Index + $mh.Length)

                # Pull the cover out of $after (if present).
                $cover = ''
                $mc = [regex]::Match($after, $coverPat)
                if ($mc.Success) {
                    # keep just the <a><img></a> (or bare <img>) — drop any <p> wrapper
                    $inner = [regex]::Match($mc.Value, '(?s)(?:<a\b[^>]*>\s*)?<img\b[^>]*\bclass="bookcover"[^>]*/?>(?:\s*</a>)?')
                    $cover = $inner.Value
                    $after = $after.Remove($mc.Index, $mc.Length)
                }

                # Split remaining markup into h5 rows (meta) vs everything else (body).
                # h5 rows go in .book-meta (centred beside the cover); the rest is
                # the body, placed after .book-head so it sits below the cover.
                $metaSb = [System.Text.StringBuilder]::new()
                foreach ($m in [regex]::Matches($after, '(?s)<h5\b.*?</h5>')) {
                    [void]$metaSb.Append($m.Value)
                }
                $body = [regex]::Replace($after, '(?s)<h5\b.*?</h5>', '')
                # A section heading (<h2>/<h3>) can appear AFTER a book (e.g. a
                # "## Honourable mentions" divider mid-list). Such a heading is part
                # of $after, so it would otherwise get wrapped inside this book's
                # .book-flow — which breaks position:sticky (it'd stick relative to
                # the small wrapper, not the page). Split it out: everything from the
                # first h2/h3 onward is emitted AFTER the book wrapper, as a
                # top-level sibling of .booklist-page (where sticky works).
                $trailing = ''
                $ms = [regex]::Match($body, '(?s)<h[23]\b')
                if ($ms.Success) {
                    $trailing = $body.Substring($ms.Index)
                    $body     = $body.Substring(0, $ms.Index)
                }
                # Tag the FIRST body paragraph as the lead (prominent blue intro).
                # The old `h5 + p` selector no longer matches now that h5s live in
                # .book-meta and the body is a separate sibling, so we mark it here.
                # BUT if the book opens with a blockquote (markdown '> …', used as
                # an attributed pull-quote), that quote IS the lead — leave it as a
                # <blockquote> and don't promote a later <p>. Only tag the first
                # <p> when no <blockquote> precedes it. Regex instance caps at 1.
                if ($body.TrimStart() -notmatch '^<blockquote') {
                    $body = ([regex]'<p>').Replace($body, '<p class="lead">', 1)
                }
                $coverHtml = if ($cover) { "<div class=`"book-cover`">$cover</div>" } else { '' }
                $metaHtml  = "<div class=`"book-meta`">$($metaSb.ToString())</div>"
                $headHtml  = "<div class=`"book-head`">$coverHtml$metaHtml</div>"
                $flowClass = 'book-flow'
                # v30: RESTORED the <div class="book"> wrapper (v29 had removed it).
                # The wrapper bounds each sticky h4 to its own book, which is what
                # makes a finished title ride up and clear instead of overlapping
                # the next one. Without it, every h4 shares the same sticky `top`,
                # so a new (one-line) h4 just paints over a previous (two-line) one
                # at that slot and the previous title's SECOND line shows below the
                # new title. v29 was removed on the theory the wrapper stopped short
                # books pinning, but the real cause then was the old fixed-offset
                # ladder; with the template's JS slot-tracker (v77) setting the h4
                # offset from the live section-bar height, short books pin fine AND
                # tall titles clear. h4 stays the FIRST child of .book so sticky
                # resolves against the wrapper.
                # $trailing (a section h2/h3 after a book) stays OUTSIDE the wrapper
                # as a direct child of .booklist-page, which its own sticky needs.
                "<div class=`"book`">$title<div class=`"$flowClass`">$headHtml$body</div></div>$trailing"
            }
            $html = $intro + ($books -join "`n")
        }
        return $html
    }

    # ------------------------------------------------------------------------
    #  IDENTITY: verify the Cloudflare Access JWT, return the email or $null.
    # ------------------------------------------------------------------------
    function Get-VerifiedUser {
        if ((Get-Cfg 'DASH_DEVMODE' 'false') -eq 'true') {
            return (Get-Cfg 'DASH_DEVEMAIL' 'rob@rob.me.uk')
        }

        $teamDomain = Get-Cfg 'CF_TEAM_DOMAIN' ''
        $accessAud  = Get-Cfg 'CF_ACCESS_AUD' ''
        if ([string]::IsNullOrWhiteSpace($teamDomain)) {
            "AUTH FAIL: CF_TEAM_DOMAIN not set" | Out-Default
            return $null
        }

        $jwt = Get-PodeHeader -Name 'Cf-Access-Jwt-Assertion'
        if ([string]::IsNullOrWhiteSpace($jwt)) {
            "AUTH FAIL: no Cf-Access-Jwt-Assertion header" | Out-Default
            return $null
        }

        $parts = $jwt.Split('.')
        if ($parts.Count -ne 3) { "AUTH FAIL: JWT not 3 parts (got $($parts.Count))" | Out-Default; return $null }

        function ConvertFrom-B64Url([string]$s) {
            $s = $s.Replace('-', '+').Replace('_', '/')
            switch ($s.Length % 4) { 2 { $s += '==' } 3 { $s += '=' } }
            return [Convert]::FromBase64String($s)
        }

        $header  = [Text.Encoding]::UTF8.GetString((ConvertFrom-B64Url $parts[0])) | ConvertFrom-Json
        $payload = [Text.Encoding]::UTF8.GetString((ConvertFrom-B64Url $parts[1])) | ConvertFrom-Json

        # fetch + cache Cloudflare's signing keys (JWKS)
        $certsUrl = "$teamDomain/cdn-cgi/access/certs"
        $jwks = $null; $jwksAge = $null
        try { $jwks = Get-PodeState -Name 'jwks'; $jwksAge = Get-PodeState -Name 'jwksAt' } catch { }
        if (-not $jwks -or -not $jwksAge -or ((Get-Date) - $jwksAge).TotalMinutes -gt 60) {
            try {
                $jwks = Invoke-RestMethod -Uri $certsUrl -TimeoutSec 10
                Set-PodeState -Name 'jwks'   -Value $jwks      | Out-Null
                Set-PodeState -Name 'jwksAt' -Value (Get-Date) | Out-Null
            } catch {
                "AUTH FAIL: JWKS fetch failed from ${certsUrl}: $_" | Out-Default
                return $null
            }
        }

        $key = $jwks.keys | Where-Object { $_.kid -eq $header.kid } | Select-Object -First 1
        if (-not $key) { "AUTH FAIL: no JWKS key matching kid=$($header.kid)" | Out-Default; return $null }

        try {
            $rsa = [Security.Cryptography.RSA]::Create()
            $rsa.ImportParameters([Security.Cryptography.RSAParameters]@{
                Modulus  = ConvertFrom-B64Url $key.n
                Exponent = ConvertFrom-B64Url $key.e
            })
            $signed = [Text.Encoding]::ASCII.GetBytes("$($parts[0]).$($parts[1])")
            $sig    = ConvertFrom-B64Url $parts[2]
            $ok = $rsa.VerifyData($signed, $sig,
                [Security.Cryptography.HashAlgorithmName]::SHA256,
                [Security.Cryptography.RSASignaturePadding]::Pkcs1)
            if (-not $ok) { "AUTH FAIL: signature did not verify" | Out-Default; return $null }
        } catch {
            "AUTH FAIL: signature verify threw: $_" | Out-Default
            return $null
        }

        $now = [DateTimeOffset]::UtcNow.ToUnixTimeSeconds()
        if ($payload.exp -and $payload.exp -lt $now) { "AUTH FAIL: token expired" | Out-Default; return $null }
        if ($payload.iss -and $payload.iss -ne $teamDomain) {
            "AUTH FAIL: issuer mismatch. token iss=$($payload.iss) expected=$teamDomain" | Out-Default; return $null
        }
        if ($accessAud) {
            $auds = @($payload.aud)
            if ($auds -notcontains $accessAud) {
                "AUTH FAIL: aud mismatch. token aud=$($auds -join ',') expected=$accessAud" | Out-Default; return $null
            }
        }

        if ($payload.email) { return [string]$payload.email }
        "AUTH FAIL: no email claim. claims=$($payload.PSObject.Properties.Name -join ',')" | Out-Default
        return $null
    }

